<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\Relations\BelongsTo;

class UserActivityLog extends Model
{
    const UPDATED_AT = null; // Only use created_at

    // Event types
    const EVENT_LOGIN = 'login';
    const EVENT_LOGOUT = 'logout';
    const EVENT_PROFILE_UPDATE = 'profile_update';
    const EVENT_PASSWORD_CHANGE = 'password_change';
    const EVENT_ORDER_PLACED = 'order_placed';
    const EVENT_CART_ADD = 'cart_add';
    const EVENT_CART_UPDATE = 'cart_update';
    const EVENT_CART_REMOVE = 'cart_remove';
    const EVENT_ADDRESS_CREATE = 'address_create';
    const EVENT_ADDRESS_UPDATE = 'address_update';
    const EVENT_ADDRESS_DELETE = 'address_delete';
    const EVENT_ADDRESS_SET_DEFAULT = 'address_set_default';

    protected $fillable = [
        'user_id',
        'session_id',
        'event_type',
        'description',
        'properties',
        'ip_address',
        'user_agent',
    ];

    protected $casts = [
        'properties' => 'array',
        'created_at' => 'datetime',
    ];

    /**
     * Get the user that performed the activity
     */
    public function user(): BelongsTo
    {
        return $this->belongsTo(User::class);
    }

    /**
     * Scope to filter by event type
     */
    public function scopeOfType($query, string $eventType)
    {
        return $query->where('event_type', $eventType);
    }

    /**
     * Scope to filter by multiple event types
     */
    public function scopeOfTypes($query, array $eventTypes)
    {
        return $query->whereIn('event_type', $eventTypes);
    }

    /**
     * Scope to get recent activities
     */
    public function scopeRecent($query, int $days = 30)
    {
        return $query->where('created_at', '>=', now()->subDays($days));
    }

