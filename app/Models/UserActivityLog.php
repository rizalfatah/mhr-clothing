    /**
     * Get event color for UI
     */
    public function getEventColorAttribute(): string
    {
        return match ($this->event_type) {
            self::EVENT_LOGIN => 'green',
            self::EVENT_LOGOUT => 'gray',
            self::EVENT_PROFILE_UPDATE, self::EVENT_PASSWORD_CHANGE => 'blue',
            self::EVENT_ORDER_PLACED => 'purple',
            self::EVENT_CART_ADD, self::EVENT_CART_UPDATE => 'yellow',
            self::EVENT_CART_REMOVE => 'red',
            self::EVENT_ADDRESS_CREATE, self::EVENT_ADDRESS_UPDATE => 'indigo',
            self::EVENT_ADDRESS_DELETE => 'red',
            self::EVENT_ADDRESS_SET_DEFAULT => 'teal',
            default => 'gray',
        };
    }
}
